function normalizeText(t) {
  return (t || "").toString().trim();
}

function looksLikeCode(t) {
  const s = normalizeText(t);
  return /^[A-Za-z0-9_-]{6,32}$/.test(s);
}

function genCode(len = 10) {
  const alphabet = "ABCDEFGHJKLMNPQRSTUVWXYZ23456789";
  let out = "";
  for (let i = 0; i < len; i++) {
    out += alphabet[Math.floor(Math.random() * alphabet.length)];
  }
  return out;
}

async function getState(chatId) {
  const raw = await redis.get(`chat:${chatId}:state`);
  if (!raw)
    return {
  access: false,
  closed: false,
  inviter: null,
  inviterName: null,
  userName: null,
  trialUsed: false,
};

  // Upstash –∏–Ω–æ–≥–¥–∞ –º–æ–∂–µ—Ç –≤–µ—Ä–Ω—É—Ç—å —É–∂–µ –æ–±—ä–µ–∫—Ç
  if (typeof raw === "object") {
   return {
  access: !!raw.access,
  closed: !!raw.closed,
  inviter: raw.inviter ?? null,
  inviterName: raw.inviterName ?? null,
  userName: raw.userName ?? null,
  trialUsed: !!raw.trialUsed,
};
  }

  try {
    const parsed = JSON.parse(raw);
    return {
  access: !!parsed.access,
  closed: !!parsed.closed,
  inviter: parsed.inviter ?? null,
  inviterName: parsed.inviterName ?? null,
  userName: parsed.userName ?? null,
  trialUsed: !!parsed.trialUsed,
};
  } catch {
    return {
      access: false,
      closed: false,
      inviter: null,
      inviterName: null,
      trialUsed: false,
    };
  }
}

